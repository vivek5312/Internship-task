function Increment(){
    let num=Number(document.getElementById('number').innerHTML);
    document.getElementById('number').innerHTML=num+1
}
function Decrement(){
    let num=Number(document.getElementById('number').innerHTML);
    document.getElementById('number').innerHTML=num-1
}
function Neutral(){
    let num=Number(document.getElementById('number').innerHTML);
    document.getElementById('number').innerHTML=0
}